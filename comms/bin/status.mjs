#!/usr/bin/env node
/*
 * status.mjs — regenerate STATUS.md from roster.jsonl + ledger.jsonl.
 *
 * STATUS.md is a GENERATED dashboard. Never hand-edit it; run this instead (or let
 * a scheduled manager pass run it). Deriving the dashboard from the ledger is what
 * stops the "dashboard drifted from reality" failure.
 *
 * Usage:  node comms/bin/status.mjs
 */
import { readFileSync, existsSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve, join } from "node:path";

const COMMS = resolve(dirname(fileURLToPath(import.meta.url)), "..");

function readJsonl(path) {
  if (!existsSync(path)) return [];
  return readFileSync(path, "utf8").split(/\r?\n/)
    .map(l => l.trim()).filter(Boolean)
    .map(l => { try { return JSON.parse(l); } catch { return null; } })
    .filter(o => o && !o._schema);
}

const roster = readJsonl(join(COMMS, "roster.jsonl"));
const ledger = readJsonl(join(COMMS, "ledger.jsonl"))
  .sort((a, b) => String(a.ts || "").localeCompare(String(b.ts || "")));

// latest roster entry per id
const agents = new Map();
for (const r of roster) if (r.id) agents.set(r.id, r);

// latest state per task
const tasks = new Map();
for (const e of ledger) {
  if (!e.task_id) continue;
  const t = tasks.get(e.task_id) || { task_id: e.task_id, owner: "", status: "open", lane: "", ts: "" };
  if (e.owner) t.owner = e.owner;
  if (e.lane) t.lane = e.lane;
  t.status = e.status || t.status;
  t.ts = e.ts || t.ts;
  tasks.set(e.task_id, t);
}

const order = { open: 0, claimed: 1, blocked: 2, review: 3, "ready-for-next": 4, complete: 5, superseded: 6 };
const rows = [...tasks.values()].sort((a, b) => (order[a.status] ?? 9) - (order[b.status] ?? 9));
const active = rows.filter(t => !["complete", "superseded"].includes(t.status));
const done = rows.filter(t => t.status === "complete");

const fmt = (s) => s.replace(/\|/g, "\\|");
let md = `# STATUS — generated dashboard\n\n`;
md += `> Generated by \`bin/status.mjs\` at ${new Date().toISOString()}. **Do not hand-edit** — rerun the script. Truth is \`ledger.jsonl\`.\n\n`;

md += `## Agents (${agents.size})\n\n`;
if (agents.size) {
  md += `| id | vendor | role | capabilities |\n|---|---|---|---|\n`;
  for (const a of agents.values())
    md += `| ${fmt(a.id)} | ${fmt(a.vendor || "?")} | ${fmt(a.role || "?")} | ${fmt((a.capabilities || []).join(", "))} |\n`;
} else md += `_No agents registered yet._\n`;

md += `\n## Active tasks (${active.length})\n\n`;
if (active.length) {
  md += `| task_id | status | owner | lane | last update |\n|---|---|---|---|---|\n`;
  for (const t of active)
    md += `| ${fmt(t.task_id)} | ${t.status} | ${fmt(t.owner || "—")} | ${fmt(t.lane || "—")} | ${t.ts || "—"} |\n`;
} else md += `_No active tasks._\n`;

md += `\n## Completed (${done.length})\n\n`;
if (done.length) {
  md += `| task_id | owner | completed |\n|---|---|---|\n`;
  for (const t of done) md += `| ${fmt(t.task_id)} | ${fmt(t.owner || "—")} | ${t.ts || "—"} |\n`;
} else md += `_None yet._\n`;

writeFileSync(join(COMMS, "STATUS.md"), md);
console.log(`STATUS.md regenerated: ${agents.size} agent(s), ${active.length} active, ${done.length} complete.`);
