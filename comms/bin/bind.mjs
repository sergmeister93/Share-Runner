#!/usr/bin/env node
/*
 * bind.mjs — one-time bootstrap when deploying this protocol into a project.
 *
 * Detects the environment, seeds the data-plane files, generates thin per-vendor
 * stubs, and (git mode) installs the pre-commit gate. CONSERVATIVE BY DESIGN: it
 * never overwrites an existing file — it scaffolds what's missing and prints what
 * you should fill in or do next. Re-running it is safe.
 *
 * Usage:  node comms/bin/bind.mjs
 */
import { existsSync, writeFileSync, readFileSync, copyFileSync, chmodSync, mkdirSync } from "node:fs";
import { execFileSync } from "node:child_process";
import { fileURLToPath } from "node:url";
import { dirname, resolve, join, basename } from "node:path";

const COMMS = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const ROOT = resolve(COMMS, "..");
const COMMS_NAME = basename(COMMS); // "comms" once renamed; "comms_new" while drafting
const log = (m) => console.log(m);
const did = [];
const todo = [];

// ----- detect substrate ---------------------------------------------------------
function hasGit() {
  if (existsSync(join(ROOT, ".git"))) return true;
  try { execFileSync("git", ["rev-parse", "--is-inside-work-tree"], { cwd: ROOT, stdio: "ignore" }); return true; }
  catch { return false; }
}
const gitPresent = hasGit();
const mode = gitPresent ? "git" : "no-vcs";
log(`comms bind — project root: ${ROOT}`);
log(`  detected: ${gitPresent ? "git repository" : "no git repo"} -> execution_mode: ${mode}`);
if (!gitPresent) todo.push("No git repo found. For the strong (git) mode + pre-commit enforcement, run `git init` on a LOCAL (non-synced) path, then re-run bind.mjs.");

// ----- seed data-plane files (never clobber) ------------------------------------
function seed(path, content) {
  if (existsSync(path)) return false;
  writeFileSync(path, content);
  did.push(`seeded ${path.replace(ROOT + "/", "").replace(ROOT + "\\", "")}`);
  return true;
}
seed(join(COMMS, "roster.jsonl"),
  JSON.stringify({ _schema: "roster-entry/v1", _doc: "append one JSON object per agent; see schemas/roster-entry.md" }) + "\n");
seed(join(COMMS, "ledger.jsonl"),
  JSON.stringify({ _schema: "ledger-entry/v1", _doc: "append one JSON object per event; see schemas/ledger-entry.md" }) + "\n");
if (mode === "no-vcs")
  seed(join(COMMS, "manifest.json"), JSON.stringify({ _schema: "manifest/v1" }, null, 2) + "\n");

// ----- vendor stubs (generated into stubs/; copy to project root yourself) ------
const stubBody = (vendor) =>
`# ${vendor} — pointer (generated by comms/bin/bind.mjs)

You are an agent in a multi-agent protocol. Your behavior contract is
\`${COMMS_NAME}/KERNEL.md\`; the project binding is \`${COMMS_NAME}/PROJECT.md\`.
Read both before acting.

Your agent id is provided at launch — if you weren't given one, stop and ask; do
not invent one. First action: register in \`${COMMS_NAME}/roster.jsonl\` and write a
\`session-start\` line in \`${COMMS_NAME}/ledger.jsonl\`.

Do NOT put behavioral rules in this file. They live in the kernel, so every vendor
shares one source of truth.
`;
mkdirSync(join(COMMS, "stubs"), { recursive: true });
const stubs = { "CLAUDE.md": "Claude Code", "AGENTS.md": "Codex", "GEMINI.md": "Gemini", ".cursorrules": "Cursor" };
for (const [file, vendor] of Object.entries(stubs)) {
  const p = join(COMMS, "stubs", file);
  if (!existsSync(p)) { writeFileSync(p, stubBody(vendor)); did.push(`generated stub stubs/${file}`); }
  if (existsSync(join(ROOT, file))) todo.push(`Root ${file} already exists — review it; replace its behavioral content with the pointer in ${COMMS_NAME}/stubs/${file} (don't duplicate rules).`);
  else todo.push(`Copy ${COMMS_NAME}/stubs/${file} to the project root so ${vendor} reads the kernel.`);
}

// ----- pre-commit hook (git mode) -----------------------------------------------
if (gitPresent) {
  try {
    const hooksDir = execFileSync("git", ["rev-parse", "--git-path", "hooks"], { cwd: ROOT }).toString().trim();
    const dst = resolve(ROOT, hooksDir, "pre-commit");
    const src = join(COMMS, "hooks", "pre-commit");
    if (!existsSync(dst)) {
      copyFileSync(src, dst);
      try { chmodSync(dst, 0o755); } catch {}
      did.push(`installed git pre-commit hook -> ${dst}`);
    } else {
      todo.push(`A pre-commit hook already exists at ${dst}. Merge in a call to \`node ${COMMS_NAME}/bin/validate.mjs\` (see ${COMMS_NAME}/hooks/pre-commit).`);
    }
  } catch (e) {
    todo.push(`Could not auto-install the pre-commit hook (${e.message}). Copy ${COMMS_NAME}/hooks/pre-commit into your .git/hooks/ manually.`);
  }
}

// ----- PROJECT.md detected values ----------------------------------------------
if (existsSync(join(COMMS, "PROJECT.md"))) {
  todo.push(`PROJECT.md exists — set: execution_mode: ${mode}; then fill Lanes, Frozen contract, Commands, Definition of Done.`);
} else {
  todo.push(`PROJECT.md missing — copy the template from the protocol kit and fill it in (execution_mode: ${mode}).`);
}

// ----- report -------------------------------------------------------------------
log(`\nDone:`);
if (did.length) did.forEach(d => log(`  ✓ ${d}`)); else log("  (nothing new to seed — already bound)");
log(`\nNext steps:`);
todo.forEach(t => log(`  → ${t}`));
log(`\nThen: launch agents, and run \`node ${COMMS_NAME}/bin/validate.mjs\` any time to check the project is honest.`);
